void CMFCApplication1Dlg::OnBnClickedSend()
{
	
	// TODO: Add your control notification handler code here

	CString chatMessage;
	GetDlgItemText(IDC_CHATBOXENTER, chatMessage);

	SetDlgItemText(IDC_EDIT_CHAT, chatMessage);

	string input = chatMessage;
	const char* pkt = input.data();
	const char* srcIP = "127.0.0.1";
	const char* destIP = "127.0.0.1";
	sockaddr_in dest;
	sockaddr_in local;
	WSAData data;
	WSAStartup(MAKEWORD(2, 2), &data);

	local.sin_family = AF_INET;
	inet_pton(AF_INET, srcIP, &local.sin_addr.s_addr);
	local.sin_port = htons(0);

	dest.sin_family = AF_INET;
	inet_pton(AF_INET, destIP, &dest.sin_addr.s_addr);
	dest.sin_port = htons(3514);

	SOCKET s = socket(AF_INET, SOCK_DGRAM, IPPROTO_UDP);
	bind(s, (sockaddr*)&local, sizeof(local));

	sendto(s, pkt, strlen(pkt), 0, (sockaddr*)&dest, sizeof(dest));

	closesocket(s);
	WSACleanup();

	//AfxMessageBox(chatMessage);
}


